#Task 2 of Assignment 3 - Using the Math Module for Calculations
import math     #importing math module in program

num = int(input("Enter a number : "))
sq_root = math.sqrt(num)
log = math.log(num)
sine = math.sin(num)

print(f"Square root of {num} is : {sq_root}.")
print(f"Logarithm of {num} is : {log}.")
print(f"Sine of {num} is : {sine}.")

